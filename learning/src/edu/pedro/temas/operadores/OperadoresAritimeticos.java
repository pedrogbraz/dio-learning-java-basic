package edu.pedro.temas.operadores;

public class OperadoresAritimeticos {
  public static void main(String[] args) {
    int soma = 1 + 1; // 2
    int subtracao = 2 - 1; // 1
    int multiplicacao = 2 * 2; // 4
    int divisao = 4 / 2; // 2
    int resto = 4 % 2; // 0
    int exponenciacao = 2 ^ 2; // 4
  }
}
